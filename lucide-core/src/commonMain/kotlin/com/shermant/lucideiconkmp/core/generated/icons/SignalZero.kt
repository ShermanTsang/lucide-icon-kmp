val signalZeroProvider = ParameterizedIconProvider { parameters ->
    buildSignalZero(parameters)
}

val SignalZero: ImageVector
    get() = signalZeroProvider.create()

val LucideIcons.SignalZero: ImageVector
    get() = signalZeroProvider.create()

private val signalZeroPath0 = PathParser().parsePathString("M2 20h.01").toNodes()

private fun buildSignalZero(parameters: IconRenderParameters = IconRenderParameters()): ImageVector {
    val iconSize = (parameters.size ?: 24.0f).dp
    val strokeWidth = parameters.strokeWidth ?: 2.0f

    return ImageVector.Builder(
        name = "signal-zero",
        defaultWidth = iconSize,
        defaultHeight = iconSize,
        viewportWidth = 24.0f,
        viewportHeight = 24.0f,
    ).apply {
        addPath(
            pathData = signalZeroPath0,
            pathFillType = PathFillType.NonZero,
            fill = null,
            stroke = SolidColor(Color.Black),
            strokeLineWidth = strokeWidth,
            strokeLineCap = StrokeCap.Round,
            strokeLineJoin = StrokeJoin.Round,
        )
    }.build()
}
