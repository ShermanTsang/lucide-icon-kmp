private val antennaPath4 = PathParser().parsePathString("M4.5 7h15").toNodes()
private val antennaPath5 = PathParser().parsePathString("M12 16v6").toNodes()

private fun buildAntenna(parameters: IconRenderParameters = IconRenderParameters()): ImageVector {
